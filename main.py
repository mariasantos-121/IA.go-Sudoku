def rodar_puzzle(nome, puzzle):
    print(f"\n{'#' * 45}")
    print(f"  Puzzle: {nome.upper()}")
    print(f"{'#' * 45}")

    board = SudokuBoard(puzzle)
    tracker = Tracker()
    agent = CSPAgent(board, tracker)

    print("\nEstado inicial:\n")
    board.print_board()

    solution = agent.solve()

    if solution:
        print("\nSolução encontrada:\n")
        board.print_board()
    else:
        print("\nSem solução para este puzzle.")

    tracker.print_summary(f"CSP Agent — {nome}")


if __name__ == "__main__":
    for nome, puzzle in puzzles.items():
        rodar_puzzle(nome, puzzle)
